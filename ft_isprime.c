#include "libft.h"

int	ft_isprime(int c)
{
	int	i;

	i = 2;
	if (c < 2)
		return (0);
	while (i <= (c / 2))
	{
		if (!(c % i))
			return (0);
		i++;
	}
	return (1);
}
