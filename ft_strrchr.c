#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r_s;

	i = 0;
	r_s = (char *)s;
	while (r_s[i])
		i++;
	while (i > -1)
	{
		if (r_s[i] == (char)c)
			return (&r_s[i]);
		i--;
	}
	return (0);
}
