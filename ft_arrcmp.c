#include "libft.h"

int	ft_arrcmp(const char **a1, const char **a2)
{
	while (*a1)
	{
		if (ft_strcmp(*a1, *a2))
			return (ft_strcmp(*a1, *a2));
		a1++;
		a2++;
	}
	return (0);
}
