#include "libft.h"

void	ft_strset(char **str, char c)
{
	int	i;

	i = 0;
	if (!str || !*str || !**str)
		return ;
	while (*(*str + i))
	{
		*(*str + i) = c;
		i++;
	}
	return ;
}
