int	ft_strclen(const char *s, char c)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	else if (!s[0])
		return (0);
	while (s[i])
	{
		if (s[i] != c)
			i++;
		else
			return (i);
	}
	return (-1);
}
